#include "../../libft.h"

int		ft_size_uni(int nb)
{
	if (nb >= 55296 && nb <= 57343)
		return (-1);
	if (nb < 128)
		return (1);
	if (nb < 2048)
		return (2);
	if (nb < 65536)
		return (3);
	if (nb <= 1114111)
		return (4);
	return (-1);
}
