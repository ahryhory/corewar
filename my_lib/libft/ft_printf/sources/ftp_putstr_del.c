#include "../../libft.h"

void	ftp_putstr_del(char *str, int *n, t_par data)
{
	int	i;

	i = 0;
	(*n) += data.c;
	while (str[i])
	{
		(*n)++;
		write(1, &str[i++], 1);
	}
	ft_strdel(&str);
}
