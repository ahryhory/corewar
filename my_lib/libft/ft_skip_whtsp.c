#include "libft.h"

char	*ft_skip_whtsp(char *str)
{
	int	n;
	int	i;

	n = 1;
	i = 0;
	while (str[i] && n)
	{
		n = 0;
		if (str[i] == ' ' || str[i] == '\t' || str[i] == '\n')
		{
			i++;
			n = 1;
		}
	}
	return (&str[i]);
}
